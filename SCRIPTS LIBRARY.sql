CREATE PROCEDURE INSERT_AUTHOR_SP
@FIRST_NAME VARCHAR(50),
@LAST_NAME VARCHAR(50)
AS
BEGIN

	DECLARE @COUNT AS INT = 0;
	SET @COUNT = (SELECT COUNT(*) FROM AUTHOR) + 1

	INSERT INTO AUTHOR
	VALUES (@COUNT, @FIRST_NAME, @LAST_NAME)
END
GO


CREATE PROCEDURE UPDATE_AUTHOR_SP
@ID INT,
@FIRST_NAME VARCHAR(50),
@LAST_NAME VARCHAR(50)
AS
BEGIN
	
	UPDATE AUTHOR
	SET FIRST_NAME = @FIRST_NAME,
		LAST_NAME = @LAST_NAME
	WHERE ID = @ID

END
GO


CREATE PROCEDURE DELETE_AUTHOR_SP
@ID INT
AS
BEGIN
	
	DELETE FROM AUTHOR
	WHERE ID = @ID

END
GO


CREATE PROCEDURE LIST_AUTHOR_SP
AS
BEGIN
	
	SELECT
		ID,
		FIRST_NAME, 
		LAST_NAME
	FROM AUTHOR

END
GO
